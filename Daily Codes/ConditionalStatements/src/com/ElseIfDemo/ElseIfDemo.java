package com.ElseIfDemo;

public class ElseIfDemo {

	public static void main(String[] args) {
		
		int marks =20;
		if(marks>=70) {
			System.out.println("A");
		}
		else if(marks>=55) {
			System.out.println("B");
		}
		else if(marks>=40) {
			System.out.println("C");
		}
		else {
			System.out.println("F");
		}

	}

}
